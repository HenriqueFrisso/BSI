    private void jSpinner1StateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_jSpinner1StateChanged
        String valor = jSpinner1.getValue().toString();
        int sua_variavel = Integer.parseInt( valor );
        if (jRadioButton2.isSelected() == true){
            if (sua_variavel < 2015){
                jSpinner1.setValue(2015);
            }else if (sua_variavel > 2038){
                jSpinner1.setValue(2038);
            }
            jSpinner2.setValue(sua_variavel+2);
        }else{
            if (sua_variavel < 2015){
                jSpinner1.setValue(2015);
            }else if (sua_variavel > 2036 && jComboBox1.getSelectedItem().equals("Sistemas de Informação")){
                jSpinner1.setValue(2036);
            }else if (sua_variavel > 2036 && (jComboBox1.getSelectedItem().equals("Sistemas de Informação") || jComboBox1.getSelectedItem().equals("Administração"))){
                jSpinner1.setValue(2036);
            } else if (sua_variavel > 2035 && jComboBox1.getSelectedItem().equals("Arquitetura")){
                jSpinner1.setValue(2035);
            }
            valor = jSpinner1.getValue().toString();
            sua_variavel = Integer.parseInt( valor );
            if ((jComboBox1.getSelectedItem().equals("Sistemas de Informação") || jComboBox1.getSelectedItem().equals("Administração"))){
                jSpinner2.setValue(sua_variavel+4);
            } else if (jComboBox1.getSelectedItem().equals("Arquitetura")){
                jSpinner2.setValue(sua_variavel+5);
            }
        }
    }//GEN-LAST:event_jSpinner1StateChanged

    private void jSpinner2StateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_jSpinner2StateChanged
        String valor = jSpinner2.getValue().toString();
        int sua_variavel = Integer.parseInt( valor );
        String valorini = jSpinner1.getValue().toString();
        int sua_variavelini = Integer.parseInt( valorini );
        if (jRadioButton2.isSelected() == true){
            if (sua_variavel < 2017){
                jSpinner2.setValue(2017);
            }else if (sua_variavel > 2040){
                jSpinner2.setValue(2040);
            }
            if (sua_variavel-sua_variavelini > 4){
                jSpinner2.setValue(sua_variavel-1);
            }else if (sua_variavel-sua_variavelini < 2){
                jSpinner2.setValue(sua_variavel+1);
            }
        }else{
            if (sua_variavel < 2019 && (jComboBox1.getSelectedItem().equals("Sistemas de Informação") || jComboBox1.getSelectedItem().equals("Administração"))){
                jSpinner2.setValue(2019);
            }else if(sua_variavel < 2020 && jComboBox1.getSelectedItem().equals("Arquitetura")){
                jSpinner2.setValue(2020);
            }else if (sua_variavel > 2040){
                jSpinner2.setValue(2040);
            }
            if ((jComboBox1.getSelectedItem().equals("Sistemas de Informação") || jComboBox1.getSelectedItem().equals("Administração")) && sua_variavel-sua_variavelini > 8){
                jSpinner2.setValue(sua_variavel-1);
            }else if (jComboBox1.getSelectedItem().equals("Arquitetura") && sua_variavel-sua_variavelini > 10){
                jSpinner2.setValue(sua_variavel-1);
            }
            if ((jComboBox1.getSelectedItem().equals("Sistemas de Informação") || jComboBox1.getSelectedItem().equals("Administração")) && sua_variavel-sua_variavelini < 4){
                jSpinner2.setValue(sua_variavel+1);
            }else if (jComboBox1.getSelectedItem().equals("Arquitetura") && sua_variavel-sua_variavelini < 5){
                jSpinner2.setValue(sua_variavel+1);
            }
        }
    }//GEN-LAST:event_jSpinner2StateChanged

