    private void adicionarNaTabela(String nome, String email, String curso, String nomeCurso, String orientador, boolean bolsa) {
        String bolsista = "";
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        if (bolsa == false && curso.equals("G")){
            modelo.addRow(new Object[]{nome, email, curso, nomeCurso});
        }
        else{
            if (bolsa == true){
                bolsista = "Sim";
            } else{
                bolsista = "Não";
            }
            modelo.addRow(new Object[]{nome, email, curso, nomeCurso, orientador, bolsista});
        }
    }
    private void cadastrar() {
        String nome, email, curso, nomeCurso, orientador = "";
        boolean bolsa;
        nome = jTextField1.getText();
        email = jTextField2.getText();
        curso = jRadioButton1.isSelected() ? "Graduação" : "Pós-Graduação";
        nomeCurso = jComboBox1.getSelectedItem().toString();
        orientador = jTextField3.getText();
        bolsa = jCheckBox1.isSelected();
        if (curso == "Graduação"){
            curso = "G";
        } else if (curso == "Pós-Graduação"){
            curso = "P";
        }
        if (nome.equals("") || email.equals("") || (curso.equals("P") && orientador.equals(""))){
            carregarErro();
        }else{
            iniciar();   
            adicionarNaTabela(nome, email, curso, nomeCurso, orientador, bolsa);
        }
    }
