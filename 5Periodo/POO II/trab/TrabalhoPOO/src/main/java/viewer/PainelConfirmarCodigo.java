    private boolean verificarCodigo() {
        String codigoDigitado = jTextField1.getText();
        String codigoCorreto = Controller.getCodigoRecuperacao();
        
        this.setVisible(false);
        if (codigoDigitado.equals(codigoCorreto)) {
            tela.alterarPainel(this, tela.recuperarCredenciais);
            return true;
        } else {
            tela.alterarPainel(this, tela.inicio);
            JOptionPane.showMessageDialog(null, 
                                          "C칩digo inv치lido.", 
                                          "Erro", 
                                          JOptionPane.ERROR_MESSAGE);
            return false;
        }
    }
